int dione_radio_preInit(void)
{
    int i;
    int ret = 0;
    int retry = 3;
	//LOG_OUT(LOG_LEVEL_ERRO, "[DIONE]radio pre init\n");

    ret = dione_write_data(keycode5, 25);
    System_Task_Delay(1);
    /*  */
	ret = dione_write_data(keycode56, 25);
	System_Task_Delay(1);

    uint8_t circuit_version;
	circuit_version = (uint8_t)getCircuitVerion();

#if (DSP_ECNR_MODE == 4)
    if(circuit_version >= VERSION_0){
        ret = dione_write_data(keycode62, 25);
        System_Task_Delay(1);
    }
#endif

	/* ECNR keycode */
#if (DSP_ECNR_MODE == 1)
	ret = dione_write_data(keycode61, 25);
	System_Task_Delay(1);
#endif

	/* ECNR keycode */
#if (DSP_ECNR_MODE == 2)
	ret = dione_write_data(keycode62, 25);
	System_Task_Delay(1);
#endif

	ret = dione_write_data(keycode80, 25);
	System_Task_Delay(1);

	ret = dione_write_data(keycode82, 25);
	System_Task_Delay(1);

	ret = dione_write_data(keycode100, 25);
	System_Task_Delay(1);

	ret = dione_write_data(keycode_53, 25);
	System_Task_Delay(1);

	ret = dione_write_data(clock_init_code,2);
	System_Task_Delay(1);

	/* TODO: boot from flash */
#if (DSP_ECNR_MODE == 1) || (DSP_ECNR_MODE == 2)
	retry = 3;
	for(i = 0; i < sizeof(boot_from_flash_command) / sizeof(UINT8); i += boot_from_flash_command[i])
	{
		while(retry)
		{
			//LOG_OUT(LOG_LEVEL_ERRO, "[DIONE]radio init cmd=0x%x\n",radio_init_data[i+1]);
			ret = saf77xx_phy_write(&boot_from_flash_command[i], boot_from_flash_command[i]);
			System_Task_Delay(50);
			if(ret >= 0)
			{
				break;
			}
			retry--;
		}
		if(retry == 0){
			break;
		}
	}
#endif

#if (DSP_ECNR_MODE == 4)
    if(circuit_version >= VERSION_0){
        retry = 3;
        for(i = 0; i < sizeof(boot_from_flash_command) / sizeof(UINT8); i += boot_from_flash_command[i])
        {
            while(retry)
            {
                //LOG_OUT(LOG_LEVEL_ERRO, "[DIONE]radio init cmd=0x%x\n",radio_init_data[i+1]);
                ret = saf77xx_phy_write(&boot_from_flash_command[i], boot_from_flash_command[i]);
                System_Task_Delay(50);
                if(ret >= 0)
                {
                    break;
                }
                retry--;
            }
            if(retry == 0){
                break;
            }
        }
    }
#endif


    ret = dione_write_data(&app_start_code,1);
    System_Task_Delay(65);      //wait system enter into idle....

    retry = 3;
    for(i = 0; i < sizeof(radio_init_data) / sizeof(UINT8); i += radio_init_data[i])
    {
        while(retry)
        {
        	//LOG_OUT(LOG_LEVEL_ERRO, "[DIONE]radio init cmd=0x%x\n",radio_init_data[i+1]);
            ret = saf77xx_phy_write(&radio_init_data[i], radio_init_data[i]);
            System_Task_Delay(1);
            if(ret >= 0)
            {
                break;
            }
            retry--;
        }
        if(retry == 0){
        	break;
        }
    }

    return ret;
}
